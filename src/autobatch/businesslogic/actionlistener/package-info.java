/**
 * Das Paket autobatch.businesslogic.actionlistener enthält Klassen, die als ActionListener fungieren und verschiedene Aktionen in der Geschäftslogik der Anwendung auslösen.
 * Die Klassen in diesem Paket implementieren das ActionListener-Interface aus dem Java Swing-Framework und reagieren auf bestimmte Aktionen, die in der Benutzeroberfläche ausgelöst werden.
 * 
 * Dieses Paket enthält die folgenden Klassen:
 * - AbgabeHinzufuegenActionListener: ActionListener zum Hinzufügen von Abgaben
 * - AblehnenActionListener: ActionListener zum Ablehnen von Anfragen
 * - AnfragenActionListener: ActionListener zum Anzeigen und Verwalten von Anfragen
 * - AnnehmenActionListener: ActionListener zum Akzeptieren von Anfragen
 * - ChangeNoteActionListener: ActionListener zum Ändern von Noten
 * - CreateAccountActionListener: ActionListener zum Erstellen eines Benutzerkontos
 * - DatenSpeichernBetreuerActionListener: ActionListener zum Speichern von Daten für Betreuer
 * - DatenSpeichernStudentActionListener: ActionListener zum Speichern von Daten für Studenten
 * - DatenSpeichernStudiendekanActionListener: ActionListener zum Speichern von Daten für Studiendekane
 * - FormularBetreuerActionListener: ActionListener zum Ausfüllen des Betreuer-Formulars
 * - FormularStudentActionListener: ActionListener zum Ausfüllen des Studenten-Formulars
 * - FormularStudiendekanActionListener: ActionListener zum Ausfüllen des Studiendekan-Formulars
 * - IPAnfrageActionListener: ActionListener zum Anfragen einer IP-Adresse
 * - IPAnnhmenActionListener: ActionListener zum Akzeptieren einer IP-Adresse
 * - LoginActionListener: ActionListener zum Durchführen des Logins
 * - NoteEintragenActionListener: ActionListener zum Eintragen von Noten
 * - RegistrationActionListener: ActionListener zur Registrierung
 */
package autobatch.businesslogic.actionlistener;